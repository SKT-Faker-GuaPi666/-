#include<iostream>
using namespace std;

//成员变量 和 成员函数 分开储存的

class Person431
{
	int m_A;        //非静态成员变量 ，属于类的对象上
	static int m_B; //静态成员变量 ，不属于类的对象上
	void func(){}   //非静态成员函数 ，不属于类的对象上
	static void func2(){} //静态成员函数，不属于类的对象上
};
void test43101()
{
	Person431 p;
	//空对象占用内存空间为：1
	//c++编译器会给每个空对象也分配一个字节空间，是为了区分空对象占用内存的位置
	//每个空对象也应该有个独一无二的内存地址
	cout << "size of p = " << sizeof(p) << endl;
}
void test43102()
{
	Person431 p;
	cout << "size of p = " << sizeof(p) << endl;
}

